class  AllType
{
	public static void main(String[] args) 
	{
		System.out.println("The value is "+20);
		System.out.println("The value is "+20+20);
		System.out.println(20+20+" is the value ");
		System.out.println("The value is "+(20+20));
		System.out.println("The value is "+(1/2));//if numerator and denominator will be (integer) then output will be also (integer)
		System.out.println("The value is "+(1/2.0));// if either numerator or denominator will be decimal then output will also be in (decimal)
	}
}
